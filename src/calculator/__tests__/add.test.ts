import { add } from '../add';

test('1 + 2 = 3', () => {
  expect(add(1, 2)).toBe(3);
});
test('-1 + 1 = 0', () => {
  expect(add(-1, 1)).toBe(0);
});
test('1000000000000000000 + 1000000000000000000 = 2000000000000000000', () => {
  expect(add(1000000000000000000, 1000000000000000000)).toBe(2000000000000000000);
});

import { minus } from '../minus';

test('3 - 2 = 1', () => {
  expect(minus(3, 2)).toBe(1);
});
test('1 - (-1) = 2', () => {
  expect(minus(1, -1)).toBe(2);
});
test('1000000000000000000 - 100000000000000000 = 900000000000000000', () => {
  expect(minus(1000000000000000000, 100000000000000000)).toBe(900000000000000000);
});
